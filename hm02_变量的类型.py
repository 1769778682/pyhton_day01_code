# 通过变量描述一个人的信息
# 姓名
name = '小明'  # 字符串
# 年龄
age = 27  # 整数
# 身高
height = 1.78  # 浮点数
# 是否为男性
sex = True  # 布尔类型
# 家庭住址
address_list = ['北京市', '昌平区']  # 列表
address_tuple = ('北京市', '昌平区')  # 元组
address_dict = {"city": '北京市', "area": '昌平区'}  # 字典

# 查看变量(数据)类型
print(type(name))
print(type(age))
print(type(height))
print(type(sex))
print(type(address_list))
print(type(address_tuple))
print(type(address_dict))
